class Compartment(object):
    """A compartment on the plate. Compartments have the following properties:

    Attributes:
        name: A string representing the well's name.
    """

    def __init__(self, name):
        """Return a Well object whose name is *name*"""
        self.name = name
        self.path = path
        self.parent = parent
        self.children = children
        self.files = files
        self.images = images
        self.commands = commands
        
